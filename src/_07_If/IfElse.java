package _07_If;

public class IfElse {
    public static void main(String[] args) {
        /*
         if (조건) {

        조건이 참이면 출력하는 구간
        }

        else{ 조건이 거짓이면 출력
        }


    */

        int height = 120;
        if (height < 120) {
            System.out.println("120보다 작다냥..");
        } else {
            System.out.println("120보다 작지 않다냥..");
        }
    }
}